# Ejemplos de comparaciones ==
print("Hola" == "Negro")  #-> False
print("Hola" == "hola")  #-> False (Case sensitive)
print("Hola".lower() == "hola")  #-> True
print("100" == 100)  #-> False
print(100 == 100.0)  #-> True

# Ejemplos de comparaciones !=
print("Hola" == "Negro")  #-> True

