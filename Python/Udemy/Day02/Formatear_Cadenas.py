x = 5
y = 10.5

# Concatenando
print("Mis números son " + str(x) + " y " + str(y))
# Antigua versión
print("Mis números son {} y {}".format(x, y))
print("La suma de los números {} y {} es {}".format(x, y, x + y))
# Con string literals
print(f"Mis números son {x} y {y}")
print(f"La suma de los números {x} y {y} es {x + y}")
